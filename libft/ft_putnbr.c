#include "libft.h"

void	ft_putnbr(int c)
{
	if (c >= 10)
	{
		ft_putnbr(c / 10);
		ft_putchar('0' + c % 10);
	}
	else if (0 <= c && c < 10)
		ft_putchar('0' + c);
	else
	{
		ft_putchar('-');
		if (c <= -10)
			ft_putnbr(-(c / 10));
		ft_putchar('0' - c % 10);
	}
}
